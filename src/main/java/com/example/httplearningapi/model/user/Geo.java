package com.example.httplearningapi.model.user;

public class Geo {
    private float lat;
    private float lon;

    public Geo() {
    }

    public float getLat() {
        return lat;
    }

    public void setLat(float lat) {
        this.lat = lat;
    }

    public float getLon() {
        return lon;
    }

    public void setLon(float lon) {
        this.lon = lon;
    }
}
